            if(todook){
                actualizarPersonajes();   
                jPanelPrincipal.setVisible(false);
                jListPersonajes.setEnabled(true);
                jButtonBorrarSeleccion.setEnabled(true);
                jButtonSeleccionar.setEnabled(true);
            }
