            int index = jListPersonajes.getSelectedIndex();

            Personaje p = lista.get(index);
            int id = p.getId();

            boolean todook = c.eliminarPersonaje(id);

            p = lista.remove(index);


            JOptionPane.showMessageDialog(null, "Has a eliminado a " + p.getNombre());
        
